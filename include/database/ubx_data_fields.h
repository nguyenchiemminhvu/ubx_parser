// ── Helper: safe offset into an array-field base ──────────────────────────────

inline data_field db_index_with_offset(data_field base, uint8_t offset)
{
    uint16_t idx = static_cast<uint16_t>(base) + static_cast<uint16_t>(offset);
    if (idx < static_cast<uint16_t>(DATA_PARSE))
    {
        return static_cast<data_field>(idx);
    }
    return DATA_PARSE;
}

