// ubx_data_fields.h
//
// Enumeration of every database field that the UBX message update handlers may
// write.  Field enum values are kept 1-to-1 compatible with the old CDatabase::DATA_t
// so that consumer code using numeric indices continues to compile unmodified.
//
// Design notes
// ─────────────
// • All field IDs are plain integers; there is NO CVar / CDatabase dependency.
// • Array fields (SAT info, MON-RF blocks, …) use contiguous ID ranges exactly
//   as in the old enum (base_id + offset).
// • DATA_PARSE acts as the "ceiling" sentinel – only ids < DATA_PARSE may be
//   written to the store.
// • DATA_NUM is the total count of allocated storage slots.
//
// Thread-safety:  plain enum; no synchronisation needed.

